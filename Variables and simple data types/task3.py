# Program with 2 numbers saved in separate variables a and b,
# then print the results of some arithmetic operations
a = 5
b = 2


# Addition
print('Addition = ' + str(a + b))
# Subtraction
print('Subtraction = ' + str(a - b))
# Division
print('Division = ' + str(a / b))
# Multiplication
print('Multiplication = ' + str(a * b))
# Exponent (Power)
print('Exponent (Power) = ' + str(a ** b))
# Modulus
print('Modulus = ' + str(a % b))
# Floor division
print('Floor division = ' + str(a // b))
